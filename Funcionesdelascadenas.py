cadena = 'Hola mundo'
print (len(cadena)) # Comprobamos la longitud de una cadena

cadena = cadena.upper() # Convertimos la cadena en mayúsculas
print (cadena)

cadena = cadena.lower() # Ponemos todas las letra en minúsculas
print (cadena)

cadena = cadena.split() # Nos crea una lista con las palabras de la cadena
print (cadena)

